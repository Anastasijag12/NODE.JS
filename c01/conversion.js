function convertMilesToKm(miles) {
    return miles * 1.6
}
console.log("Miles to kilometers", convertMilesToKm(1))

function convertCelsiusToFahrenheit(celsuis) {
    return celsuis * 1.8
}
console.log("Celsuis to Fahrenheit", convertCelsiusToFahrenheit(1))

function convertPoundsToKilogram(pounds) {
    return pounds * 0.45
}
console.log("Pounds to Kilogram", convertPoundsToKilogram(1))

function convertFeetToMetres(feet) {
    return feet * 0.3
}
console.log("Feet to Kilogram", convertFeetToMetres(1))